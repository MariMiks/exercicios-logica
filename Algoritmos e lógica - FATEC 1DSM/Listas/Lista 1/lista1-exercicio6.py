#6) Calcule o tempo de uma viagem de carro. Pergunte a distância a percorrer e
#a velocidade média esperada para a viagem

d = float(input("Qual a distância percorrida? "))
vm = float(input("Qual a velocidade média esperada? "))
tempo = d/vm
print(f"O tempo da viagem será aproximadamente: {tempo}s")
